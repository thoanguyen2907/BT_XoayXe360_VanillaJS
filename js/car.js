export class Car {
    constructor(id, title, type, img, srcImg, color, price, engineType, displacement,
        horsepower, torque, redline, wheels) {
        this.id = id;
        this.title = title;
        this.type = type;
        this.img = img;
        this.srcImg = srcImg;
        this.color = color;
        this.price = price;
        this.engineType = engineType;
        this.displacement = displacement;
        this.horsepower = horsepower;
        this.torque = torque;
        this.redline = redline;
        this.wheels = wheels;
    }
}